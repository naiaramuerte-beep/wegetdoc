let content = readFileSync(filePath, 'utf-8');
const contentLines = content.split('\n');

// Find all line numbers with editor_toast_password_protected (skip first which is type def)
const markerLines = [];
for (let i = 0; i < contentLines.length; i++) {
  if (contentLines[i].includes('editor_toast_password_protected') && contentLines[i].includes('"')) {
    markerLines.push(i);
  }
}

console.log(`Found ${markerLines.length} language blocks with marker`);
console.log('Line numbers:', markerLines.map(l => l + 1));

if (markerLines.length !== langOrder.length) {
  console.error(`Expected ${langOrder.length} language blocks, found ${markerLines.length}`);
  process.exit(1);
}

// Insert from bottom to top so line numbers don't shift
for (let i = markerLines.length - 1; i >= 0; i--) {
  const lang = langOrder[i];
  const lineIdx = markerLines[i];
  const translationBlock = translations[lang];
  if (!translationBlock) {
    console.log(`No translations for ${lang}, skipping`);
    continue;
  }
  // Insert after the marker line
  contentLines.splice(lineIdx + 1, 0, translationBlock);
  console.log(`Inserted ${lang} translations after line ${lineIdx + 1}`);
}

writeFileSync(filePath, contentLines.join('\n'));
console.log('Done! All panel translations added correctly.');
