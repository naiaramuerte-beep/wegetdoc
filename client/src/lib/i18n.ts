/** Detect language from URL path prefix, e.g. /en/... → "en" */
export function detectLangFromPath(pathname: string): LangCode {
  const match = pathname.match(/^\/([a-z]{2})(\/|$)/);
  if (match) {
    const code = match[1] as LangCode;
    if (LANGUAGES.find(l => l.code === code)) return code;
  }
  return "es"; // default
}

/** Detect language from browser navigator */
export function detectLangFromBrowser(): LangCode {
  const nav = navigator.language?.slice(0, 2).toLowerCase() as LangCode;
  if (LANGUAGES.find(l => l.code === nav)) return nav;
  return "es";
}

/** Build a URL with the given language prefix */
export function buildLangUrl(lang: LangCode, path: string = "/"): string {
  // Remove existing lang prefix if present
  const cleanPath = path.replace(/^\/[a-z]{2}(\/|$)/, "/");
  return `/${lang}${cleanPath === "/" ? "" : cleanPath}`;
}
